void delay_us( uint32_t n )
{
    uint16_t    i = 5;

    while ( n-- )
    {
        while( i-- )
        {
        	asm("nop");
        }
		
        i = 5;
    }
}

void delay_ms( uint32_t n )
{
    uint16_t    i = 2000;

    while ( n-- )
    {
        while( i-- )
		{
			asm("nop");
		}
		
        i = 2000;
    }
}
